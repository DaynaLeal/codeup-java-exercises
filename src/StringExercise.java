import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args){
        //exercise 1 String Basics:
        //For each of the following output examples: create a String variable that contains the desired output and print it out to the console, you can do this with only one String variable and one print statement for each output example.

        //We don't need no education
        //We don't need no thought control
        String weDontNeed = "We don't need no education\nWe don't need no thought control";
        System.out.println(weDontNeed);

        //Check "this" out!, "s inside of "s!
        String checkThis = "Check \"this\" out!, \"s inside of \"s";
        System.out.println(checkThis);

        //In windows, the main drive is usually C:\
        String inWindows = "In windows, the main drive is usually C:\\";
        System.out.println(inWindows);

        //I can do backslashes \, double backslashes \\,
        //and the amazing triple backslash \\\!
        String backslashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
